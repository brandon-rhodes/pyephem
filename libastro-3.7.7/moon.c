/* Reduce arc seconds modulo 360 degrees
   answer in arc seconds  */
static double
mods3600(double x)
{
  double y;
#if !defined(__STDC__)
  double floor();
#endif

  y = x - 1296000. * floor( x/1296000.);
  return(y);
}


